let backgroundImage, grid;

function preload() {
  backgroundImage = loadImage("images/pacman-grid.png");
}

function setup() {
  createCanvas(windowWidth, windowHeight);
}

function draw() {
  makeGrid();
}

function makeGrid() {
  for (let x = 0; x < 27; x++) {
    for (let y = 0; y < 21; y++) {
      if (startingGrid[y][x] === 1) {
        fill("blue");
      } // Schellenberg said to add an image as a background to make it look like the original game
      if (startingGrid[y][x] === 0) {
        fill(0);
      }
      rect(x * 25, y * 25, 25, 25);
    }
  }
}
